import {NotificationEvent, NotificationMethod} from "../config/recruitmentConfig";
import {Notifier} from "./Notifier";
import {Compensator} from "./Compensator";
import {NotificationDAO} from "./DAO/DAOdefs";
import {ConfigManager} from "./ConfigManager";
import {GMailNotifier} from "./GMailNotifier";
import Timeout = NodeJS.Timeout;
import {NotificationTemplates} from "../config/notificationTemplate";
